          <h2>ITCS Mission Statement </h2>
		<p>Our mission is to form a community of students with an interest for food. The club will create an opportunity for students to hang out and cook together while sharing their own culinary knowledge and experiences with others. This society is a student run organization that plans recreational and educational activities for basic culinary and baking technique. You don't have be in the Culinary Program, this club is open to anyone who are interested in cooking, baking.</p>




	<h2>Goal</h2>
	<p>Our main goal to bring students to a deeper understanding about the foods they eat. To create a culmination of recipes from around the world. To expand our club interest to connect with food justice issues, sustainability, and culture. Also, to organize on the college campus and to interact with in the SIPI community so that we can organize events that raise awareness about eating healthy and sustainability. And to have fun!</p>

	<h1>Culinary Arts Program at SIPI</h1>
	<p>The Associate in Applied Science Degree program in Culinary Arts is a course of study over five (5) trimesters designed for students preparing for entry-level food service management positions or higher education in the field. The degree program is designed to provide students with a broad range of management, business and technical skills applicable to the food service industry. The curriculum emphasizes fundamental skills needed in the culinary and baking and pastry industries, food service management, basic supervisory practices, customer relations, nutrition. Students are introduced to the gastronomical sciences involved in the preparation of cooking and baking; along with those of raw materials and equipment to produce quality food products and services efficiently.</p>


<h2>Mission Statement</h2>
<p>It is the mission of the Culinary Arts Associate of Applied Science Degree Program at Southwestern Indian Polytechnic institute to promote student success by providing a culinary education with a management component with emphases on culinary fundamentals that will build culinary skill, knowledge, self-confidence, and pride. Our graduates will gain the experience and knowledge that will lead to professional culinary careers.</p>

<h2>Vision Statement</h2>
<p>The Vision of the culinary arts program at Southwestern Indian Polytechnic Institute is to seek accreditation through the American Culinary Federation Educational Foundation (ACFEF). The Culinary Arts program will work towards becoming the premiere Native American culinary arts training program within the federal Government.</p>

